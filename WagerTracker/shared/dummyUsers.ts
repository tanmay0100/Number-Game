export const colorOptions = ['red', 'green', 'blue', 'yellow'];
export const betAmounts = [10, 20, 25, 30, 40, 50, 75, 100, 150, 200];

// Generate random combinations
export function getRandomDummyBet() {
  const userName = dummyUserNames[Math.floor(Math.random() * dummyUserNames.length)];
  
  // 70% chance for single color, 30% chance for double color (NO triple colors)
  const numColors = Math.random() < 0.7 ? 1 : 2;
  const selectedColors = [];
  const availableColors = [...colorOptions];
  
  for (let i = 0; i < numColors; i++) {
    const colorIndex = Math.floor(Math.random() * availableColors.length);
    selectedColors.push(availableColors.splice(colorIndex, 1)[0]);
  }
  
  const betAmount = betAmounts[Math.floor(Math.random() * betAmounts.length)];
  
  return {
    userName,
    selectedColors: selectedColors.join(', '),
    betAmount,
    gameType: 'color-king',
    roundNumber: 1
  };
}